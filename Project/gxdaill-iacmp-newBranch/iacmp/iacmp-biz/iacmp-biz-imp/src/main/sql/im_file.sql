CREATE TABLE `im_file` (
  `ID` varchar(32) NOT NULL,
  `FILE_NAME` varchar(500) DEFAULT NULL COMMENT '文件名称',
  `STATUS` varchar(1) DEFAULT NULL COMMENT '文件状态（0-正常；1-已废弃）',
  `BATCH_ID` varchar(32) DEFAULT NULL COMMENT '文件批次',
  `BUSINO` varchar(64) DEFAULT NULL COMMENT '影像流水号',
  `REGION_NO` varchar(64) DEFAULT NULL COMMENT '分片建哈希值',
  `CREATE_TIME` datetime DEFAULT NULL COMMENT '文件上传时间',
  `SERIALNO` decimal(38,0) DEFAULT NULL COMMENT '文件顺序',
  `FILE_PATH` varchar(2000) DEFAULT NULL COMMENT '文件存放路径',
  `FILE_SIZE` decimal(9,0) DEFAULT NULL COMMENT '文件大小',
  `FILE_TYPE` varchar(32) DEFAULT NULL COMMENT '文件类型',
  `BILL_ID` varchar(32) DEFAULT NULL COMMENT '所属单据类型',
  `BILL_NAME` varchar(2000) DEFAULT NULL COMMENT '单据类型名称',
  `REMARK` varchar(2000) DEFAULT NULL COMMENT '备注',
  `CREATE_USER` varchar(100) DEFAULT NULL COMMENT '上传人编号',
  `CREATE_USER_NAME` varchar(1000) DEFAULT NULL COMMENT '上传人名称',
  `DELETE_TIME` datetime DEFAULT NULL COMMENT '删除时间',
  `SYSTEM_FLAG` varchar(100) DEFAULT NULL COMMENT '所属系统',
  `FILE_SOURCE` varchar(1000) DEFAULT NULL COMMENT '文件来源',
  `sys_flag_int` int(4) NOT NULL COMMENT '系统标识状态',
  `FILE_ID` bigint(20) NOT NULL AUTO_INCREMENT COMMENT '主键',
  PRIMARY KEY (`FILE_ID`),
  UNIQUE KEY `ID` (`ID`),
  KEY `3333333` (`BATCH_ID`) USING BTREE,
  KEY `IM_BATCH_INDEX1` (`BATCH_ID`) USING BTREE,
  KEY `IM_FILE_INDEX2` (`CREATE_TIME`) USING BTREE
) ENGINE=InnoDB AUTO_INCREMENT=1 DEFAULT CHARSET=utf8;