CREATE TABLE `im_image` (
  `ID` varchar(32) NOT NULL COMMENT '影像主键',
  `IMAGE_NAME` varchar(500) DEFAULT NULL COMMENT '影像名称',
  `STATUS` varchar(1) DEFAULT NULL COMMENT '影像状态（0-正常；1-已废弃）',
  `BATCH_ID` varchar(32) DEFAULT NULL COMMENT '批次ID',
  `BUSINO` varchar(64) DEFAULT NULL COMMENT '影像流水号',
  `REGION_NO` varchar(64) DEFAULT NULL COMMENT '分片建哈希值',
  `CREATE_TIME` datetime DEFAULT NULL COMMENT '影像上传时间',
  `SERIALNO` decimal(38,0) DEFAULT NULL COMMENT '影像顺序',
  `WIDTH` varchar(100) DEFAULT NULL COMMENT '影像宽度',
  `HEIGHT` varchar(100) DEFAULT NULL COMMENT '影像高度',
  `BILL_ID` varchar(32) DEFAULT NULL COMMENT '单据类型',
  `TEMPLATE_ID` varchar(32) DEFAULT NULL COMMENT '影像模版ID',
  `IMAGE_SIZE` decimal(8,0) DEFAULT '0' COMMENT '影像大小',
  `IMAGE_PATH` varchar(2000) DEFAULT NULL COMMENT '影像存放路径',
  `OCR_RESULT` longtext COMMENT '识别结果',
  `OCR_STATUS` varchar(10) DEFAULT NULL COMMENT '识别状态(0-待识别;1-已识别;)',
  `BILL_NAME` varchar(500) DEFAULT NULL COMMENT '单据类型名称',
  `REMARK` varchar(2000) DEFAULT NULL COMMENT '备注',
  `CREATE_USER` varchar(100) DEFAULT NULL COMMENT '上传人编号',
  `CREATE_USER_NAME` varchar(1000) DEFAULT NULL COMMENT '上传人名称',
  `DELETE_TIME` datetime DEFAULT NULL COMMENT '删除时间',
  `SYSTEM_FLAG` varchar(100) DEFAULT NULL COMMENT '所属系统',
  `DELETE_USER` varchar(64) DEFAULT NULL COMMENT '删除人编号',
  `DELETE_USER_NAME` varchar(64) DEFAULT NULL COMMENT '删除人姓名',
  `PZ_NUM` int(38) DEFAULT '0' COMMENT '批注数量',
  `IMAGE_SOURCE` varchar(1000) DEFAULT NULL COMMENT '影像来源',
  `SYS_FLAG_INT` int(4) NOT NULL COMMENT '系统标识状态',
  `OCR_TYPE` varchar(64) DEFAULT NULL COMMENT '识别类型',
  `IMAGE_ID` bigint(20) NOT NULL AUTO_INCREMENT COMMENT '主键',
  PRIMARY KEY (`IMAGE_ID`),
  UNIQUE KEY `ID` (`ID`),
  KEY `IM_IMAGE_INDEX1` (`BATCH_ID`) USING BTREE,
  KEY `IM_IMAGE_INDEX0` (`CREATE_TIME`,`IMAGE_SIZE`) USING BTREE
) ENGINE=InnoDB AUTO_INCREMENT=1 DEFAULT CHARSET=utf8;